let parallelReplyCursor = 0
export function nextDemoParallelReply(userMessage: string): string {
  // 简单关键词命中，让对话更"懂你"
  const text = userMessage.toLowerCase()
  if (/后悔|遗憾|可惜/.test(userMessage)) {
    return DEMO_PARALLEL_REPLIES[2]
  }
  if (/羡慕|更好|比较/.test(userMessage)) {
    return DEMO_PARALLEL_REPLIES[1]
  }
  if (/累|难|疼|苦|哭/.test(userMessage) || /tired|hard/.test(text)) {
    return DEMO_PARALLEL_REPLIES[5]
  }
  const reply = DEMO_PARALLEL_REPLIES[parallelReplyCursor % DEMO_PARALLEL_REPLIES.length]
  parallelReplyCursor += 1
  return reply
}

export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

